std::tuple<int, int, int> egcd(int a, int b) {
    /*
     * Extended euclidian algorithm result for a and b.
     * m, ka, kb = egcd(a, b)
     * ka * a + kb * b == m == gcd(a, b)
     */

    int x0 = 1;
    int x1 = 0;
    int y0 = 0;
    int y1 = 1;

    while (a != 0 and b != 0) {
        int q = a / b;

        int na = b;
        int nb = a % b;

        a = na;
        b = nb;

        int nx0 = x1;
        int nx1 = x0 - q * x1;
        x0 = nx0;
        x1 = nx1;
        int ny0 = y1;
        int ny1 = y0 - q * y1;

        y0 = ny0;
        y1 = ny1;
    }
    return {a, x0, y0};
}

